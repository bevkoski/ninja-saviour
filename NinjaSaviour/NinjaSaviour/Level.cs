        public Level(ContentManager contentManager, int windowWidth, int windowHeight,
            string spriteName, int level, Boss boss)
        {
            // set window dimensions
            this.windowWidth = windowWidth;
            this.windowHeight = windowHeight;

            // set draw rectangle location and dimensions and load content
            levelRectangle.X = 0;
            levelRectangle.Y = 0;
            levelRectangle.Width = windowWidth;
            levelRectangle.Height = windowHeight;
            LoadContent(contentManager, spriteName);

            // initialize lists
            this.level = level;
            shurikens = new List<Shuriken>();
            enemies = new List<Enemy>();
            this.boss = boss;

            // set data
            if (level == 1)
            {
                grid = gridOne;
                initializeShurikens(contentManager, shurikensOne, true);
                initializeEnemies(contentManager, enemiesOne);
            }
            else if (level == 2)
            {
                grid = gridTwo;
                initializeShurikens(contentManager, shurikensTwo, true);
                initializeEnemies(contentManager, enemiesTwo);
            }
            else if (level == 3)
            {
                grid = gridThree;
                initializeShurikens(contentManager, shurikensThree, true);
                initializeEnemies(contentManager, enemiesThree);
            }
            else if (level == 4)
            {
                grid = bossGrid;
                initializeShurikens(contentManager, bossShurikens, false);
                levelTexture3 = contentManager.Load<Texture2D>("bosslevel3");
                levelTexture2 = contentManager.Load<Texture2D>("bosslevel2");
                levelTexture1 = contentManager.Load<Texture2D>("bosslevel1");
            }
        }

        public int[,] getGrid
