        private void LoadContent(ContentManager contentManager, string spriteName)
        {
            // load content
            levelTexture = contentManager.Load<Texture2D>(spriteName);
        }

        public void Update(GameTime gameTime, Ninja ninja)
        {
            // update shurikens
            foreach (Shuriken shuriken in shurikens)
            {
                shuriken.Update(gameTime, ninja, grid);
            }

            // update enemies
            foreach (Enemy enemy in enemies)
            {
                if (enemy.Moving == 1)
                    enemy.Update(gameTime, grid);
            }

            // check for a collision between a shuriken and an enemy
            foreach (Shuriken shuriken in shurikens)
            {
                if (shuriken.Flying)
                {
                    foreach (Enemy enemy in enemies)
                    {
                        if (enemy.Active
                            && Math.Abs(enemy.CollisionRectangle.Center.X - shuriken.CollisionRectangle.Center.X) < 20
                            && Math.Abs(enemy.CollisionRectangle.Center.Y - shuriken.CollisionRectangle.Center.Y) < 20)
                        {
                            enemy.Active = false;
                            enemy.Dying = true;
                            shuriken.Flying = false;
                        }
                    }

                    if (boss != null && boss.Active)
                    {
                        if (Math.Abs(boss.CollisionRectangle.Center.X - shuriken.CollisionRectangle.Center.X) < 40
                            && Math.Abs(boss.CollisionRectangle.Center.Y - shuriken.CollisionRectangle.Center.Y) < 40)
                        {
                            boss.Life--;
                            if (boss.Life == 2)
                                levelTexture = levelTexture2;
                            else if (boss.Life == 1)
                                levelTexture = levelTexture1;
                            shuriken.Flying = false;
                        }
                    }
                }
            }
        }

        public void Draw(SpriteBatch spriteBatch)
        {
            spriteBatch.Draw(levelTexture, levelRectangle, Color.White);
            
            // draw shurikens
            foreach (Shuriken shuriken in shurikens)
            {
                shuriken.Draw(spriteBatch);
            }

            // draw enemies
            foreach (Enemy enemy in enemies)
            {
                enemy.Draw(spriteBatch);
            }
        }

        private void initializeShurikens(ContentManager contentManager, int[,] data, bool active)
        {
            try
            {
                shurikens = new List<Shuriken>();
                for (int i = 0; i < data.Length; i++)
                {
                    Shuriken shuriken = new Shuriken(contentManager, "shuriken", data[i, 0], data[i, 1], active);
                    shurikens.Add(shuriken);
                }
            }
            catch (Exception e)
            {
                Console.WriteLine(e.Message);
            }
        }

        private void initializeEnemies(ContentManager contentManager, int[,] data)
        {
            try
            {
                enemies = new List<Enemy>();
                for (int i = 0; i < data.Length; i++)
                {
                    Enemy enemy = new Enemy(contentManager, "enemies", data[i, 0], data[i, 1], data[i, 2], data[i, 3], data[i, 4]);
                    enemies.Add(enemy);
                }
            }
            catch (Exception e)
            {
                Console.WriteLine(e.Message);
            }
        }

        public bool checkDeath(Ninja ninja, Boss boss)
        {
            foreach (Enemy enemy in enemies)
            {
                if (enemy.Active
                    && Math.Abs(enemy.CollisionRectangle.Center.X - ninja.CollisionRectangle.Center.X) < 40
                    && Math.Abs(enemy.CollisionRectangle.Center.Y - ninja.CollisionRectangle.Center.Y) < 40)
                    return true;
            }

            if (boss.Active
                && Math.Abs(boss.CollisionRectangle.Center.X - ninja.CollisionRectangle.Center.X) < 80
                && Math.Abs(boss.CollisionRectangle.Center.Y - ninja.CollisionRectangle.Center.Y) < 80)
                return true;

            if (boss.Active && boss.checkCollision(ninja))
                return true;

            return false;
        }

        public void resetLevel(ContentManager contentManager)
        {
            // initialize lists
            shurikens = new List<Shuriken>();
            enemies = new List<Enemy>();

            // set data
            if (level == 1)
            {
                grid = gridOne;
                initializeShurikens(contentManager, shurikensOne, true);
                initializeEnemies(contentManager, enemiesOne);
            }
            else if (level == 2)
            {
                grid = gridTwo;
                initializeShurikens(contentManager, shurikensTwo, true);
                initializeEnemies(contentManager, enemiesTwo);
            }
            else if (level == 3)
            {
                grid = gridThree;
                initializeShurikens(contentManager, shurikensThree, true);
                initializeEnemies(contentManager, enemiesThree);
            }
            else if (level == 4)
            {
                levelTexture = levelTexture3;
                grid = bossGrid;
                initializeShurikens(contentManager, bossShurikens, false);
                boss.initializeProjectiles(contentManager);
            }
        }

        public void setShuriken(int X, int Y)
        {
            if (!(shurikens[0].Active || shurikens[0].Flying))
            {
                shurikens[0].Active = true;
                shurikens[0].Xpos = X;
                shurikens[0].Ypos = Y;
            }
        }
